SELECT
	INV.ID  PROJ_ID
	, INV.CODE PROJ_CODE
	, INV.NAME PROJ_NAME
	, 'VPL (' || INV.CURRENCY_CODE || ' MM)' LEGENDA
	, TO_CHAR((OPRJ.DITV_PS_VPL / 1000), '999999990D999') VPL_PESSIMISTA
	, TO_CHAR((OPRJ.DITV_MD_VPL / 1000), '999999990D999') VPL_MODERADO
	, TO_CHAR((OPRJ.DITV_OT_VPL / 1000), '999999990D999') VPL_OTIMISTA

FROM
	INV_INVESTMENTS INV
	LEFT JOIN ODF_CA_PROJECT OPRJ ON OPRJ.ID = INV.ID

WHERE 
	INV.ID = 5058068